async function getCommits() {
    let page = 1;
    const responses = [];

    try {
        while (true) {
            const response = await fetch(
                `https://api.github.com/repos/WhiteEyedFly/portfolio/commits?per_page=100&page=${page}`
            );
            if (!response.ok) break;
            const commitList = await response.json();
            if (!commitList || commitList.length === 0) break;
            responses.push(...commitList);
            page += 1;
        }
        if (responses.length === 0) throw new Error("Empty API response");
        return responses.map(commit => ({
            sha: commit.sha,
            message: commit.commit.message,
            date: commit.commit.author.date,
            author: commit.commit.author.name,
            parents: commit.parents.map(parent => parent.sha)
        }));
    } catch (e) {
        // Fall back to stored if rate limit hit
        console.warn("GitHub API rate limit hit or offline. Loading local mock commit timeline data.");
        return backUpCommits;
    }
}

async function getBranches() {
    const cachedData = localStorage.getItem("github_branches");
    const cachedTime = localStorage.getItem("github_branches_timestamp");
    const now = Date.now();

    if (cachedData && cachedTime && (now - cachedTime < CACHE_EXPIRY_MS)) {
        return JSON.parse(cachedData);
    }

    try {
        const res = await fetch("https://api.github.com/repos/WhiteEyedFly/portfolio/branches?per_page=100&page=1");
        if (!res.ok) throw new Error();
        const data = await res.json();
        
        localStorage.setItem("github_branches", JSON.stringify(data));
        localStorage.setItem("github_branches_timestamp", now.toString());
        return data;
    } catch (e) {
        return backUpBranches;
    }
}

function markReachable(commitSha, branchName, map, visited = new Set()) {
    if (!commitSha || visited.has(commitSha)) return;
    visited.add(commitSha);

    const commit = map.get(commitSha);
    if (!commit) return;

    commit.branches ??= [];
    if (!commit.branches.includes(branchName)) {
        commit.branches.push(branchName);
    }

    for (const parent of commit.parents) {
        markReachable(parent, branchName, map, visited);
    }
}

async function main() {
    const branches = await getBranches();
    const commits = await getCommits();

    const parseDate = d => new Date(d.date);

    // Sort chronologically (Newest elements map to the top coordinates)
    commits.sort((a, b) => parseDate(b) - parseDate(a));

    const commitMap = new Map(commits.map(c => [c.sha, c]));
    const branchHeads = new Map(branches.map(b => [b.name, b.commit.sha]));

    for (const [branchName, headSha] of branchHeads) {
        markReachable(headSha, branchName, commitMap);
    }

    const uniqueBranches = branches.map(b => b.name);
    
    // --------------------------------------------------------------------------
    // FIX: BULLETPROOF MATHEMATICAL DATE EXTREMES LOOKUP
    // --------------------------------------------------------------------------
    const newestDate = d3.max(commits, parseDate);
    const oldestDate = d3.min(commits, parseDate);
    
    // Calculate the day span safely using timestamps
    const totalTimeDiff = Math.abs(newestDate - oldestDate);
    const daySpan = Math.ceil(totalTimeDiff / (1000 * 60 * 60 * 24));
    
    // Allocate 60px per calendar day for breathing room, with a 600px floor
    // If multiple commits happen on the same day, we add extra padding per commit
    const baseHeight = Math.max(600, daySpan * 30);
    const computedHeight = baseHeight + (commits.length * 20);

    // Wipe previous DOM instances and bind the updated container geometry
    const targetElement = d3.select("#graphVisualiser").html("");
    
    const svg = targetElement.append("svg")
        .attr("width", "100%")
        .attr("height", `${computedHeight}px`)
        .style("background-color", "rgba(208, 208, 208, 0.45)")
        .style("border", "3px solid #484848")
        .style("border-radius", "7px");

    // Dynamic horizontal lanes configuration tracking
    const getBranchXCoordinate = (d) => {
        // Fix array parsing: look at first branch or fallback
        const primaryBranch = d.branches && d.branches.length ? d.branches[0] : "main";
        const branchIndex = uniqueBranches.indexOf(primaryBranch);
        return 60 + (branchIndex >= 0 ? branchIndex * 40 : 0);
    };

    // FIX: Injected a robust 60px viewport bounding margin padding buffer
    const yScale = d3.scaleTime()
        .domain([newestDate, oldestDate]) 
        .range([60, computedHeight - 60]);

    const getCommitYCoordinate = (d) => yScale(parseDate(d));

    // --------------------------------------------------------------------------
    // RENDER LAYER A: CONNECTOR VECTOR EDGES (Git Tracks)
    // --------------------------------------------------------------------------
    commits.forEach((commit) => {
        const startX = getBranchXCoordinate(commit);
        const startY = getCommitYCoordinate(commit);

        commit.parents.forEach(parentSha => {
            const parentIndex = commits.findIndex(c => c.sha === parentSha);
            if (parentIndex !== -1) {
                const endX = getBranchXCoordinate(commits[parentIndex]);
                const endY = getCommitYCoordinate(commits[parentIndex]);

                svg.append("line")
                    .attr("x1", startX)
                    .attr("y1", startY)
                    .attr("x2", endX)
                    .attr("y2", endY)
                    .attr("stroke", "#484848")
                    .attr("stroke-width", 2.5)
                    .attr("opacity", 0.7);
            }
        });
    });

    // --------------------------------------------------------------------------
    // RENDER LAYER B: TIMELINE ACTIONS & ACCURATE ANNOTATIONS
    // --------------------------------------------------------------------------
    const commitGroups = svg.append("g")
        .selectAll(".commit-node")
        .data(commits)
        .join("g")
        .classed("commit-node", true);

    // Circle Timeline Nodes
    commitGroups.append("circle")
        .attr("cx", d => getBranchXCoordinate(d))
        .attr("cy", d => getCommitYCoordinate(d))
        .attr("r", 6.5)
        .attr("fill", d => {
            const b = d.branches || [];
            if (b.includes("main") || b.includes("master")) return "#95a472"; // Olive Green
            return b.length ? "#4a90e2" : "#757575";
        })
        .attr("stroke", "#484848")
        .attr("stroke-width", 2);

    // Commit Message String Alignment Rows
    commitGroups.append("text")
        .attr("x", 60 + (uniqueBranches.length * 40) + 20) // Keeps labels cleanly isolated to the right
        .attr("y", d => getCommitYCoordinate(d) + 5)
        .attr("fill", "#1a1d13")
        .style("font-family", "sans-serif")
        .style("font-size", "13px")
        .style("font-weight", "700")
        .text(d => {
            const cleanDate = new Date(d.date).toLocaleDateString(undefined, {month: 'short', day: 'numeric', hour: '2-digit', minute:'2-digit'});
            const displayMsg = d.message;
            return `[${cleanDate}] ${displayMsg}`;
        });

    // Interactive Floating Info Tooltips
    commitGroups.append("title")
        .text(d => `Commit SHA: ${d.sha.slice(0, 7)}\nAuthor: ${d.author}\nTimestamp: ${d.date}\nMessage: ${d.message}`);

    console.log("Auto-scaling vertical canvas operational.");
}

main();
